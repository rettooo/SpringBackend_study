package Head01_javaprogrammingstart.practice;

public class LoopEx {
    //1부터 10까지 출력 for문 while 문
    public static void main(String[] args) {
        for (int i =1; i <=5; i++){
            System.out.println(i);
        }
        System.out.println();
        int count = 1;
        while (count < 6){
            System.out.println(count);
            count++;
        }
    }

}
